'use client';

import { Fragment, useRef, useState, useEffect } from 'react';
import { Dialog, Transition } from '@headlessui/react';
import { UserPlus, Shield, Globe, Lock, Key } from 'lucide-react';
import { Input } from '@/components/ui/input';
import { Button } from '@/components/ui/button';
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from '@/components/ui/select';
import { useSuperAdminService, RecentTenant, Role, UserCreateCrossTenant } from '@/services/api/super-admin-service';
import { toast } from 'sonner';

interface UserCreateModalProps {
    isOpen: boolean;
    isLoading?: boolean;
    onSave: (userData: UserCreateCrossTenant) => void;
    onCancel: () => void;
}

export default function UserCreateModal({
    isOpen,
    isLoading = false,
    onSave,
    onCancel,
}: UserCreateModalProps) {
    const cancelButtonRef = useRef(null);
    const superAdminService = useSuperAdminService();

    const [formData, setFormData] = useState({
        first_name: '',
        last_name: '',
        email: '',
        tenant_id: '',
        role_id: '',
        generatePassword: true,
        customPassword: ''
    });

    const [tenants, setTenants] = useState<RecentTenant[]>([]);
    const [roles, setRoles] = useState<Role[]>([]);
    const [fetchingData, setFetchingData] = useState(false);
    const [errors, setErrors] = useState<{ [key: string]: string }>({});

    useEffect(() => {
        if (isOpen) {
            loadData();
        }
    }, [isOpen]);

    const loadData = async () => {
        setFetchingData(true);
        try {
            const [tenantsRes, rolesRes] = await Promise.all([
                superAdminService.getTenants({ limit: 100 }),
                superAdminService.getRoles()
            ]);
            setTenants(tenantsRes.items);
            setRoles(rolesRes);
        } catch (error) {
            console.error('Failed to load tenants or roles:', error);
            toast.error('Failed to load metadata. Please try again.');
        } finally {
            setFetchingData(false);
        }
    };

    const handleInputChange = (field: string, value: any) => {
        setFormData(prev => ({ ...prev, [field]: value }));
        if (errors[field]) {
            setErrors(prev => ({ ...prev, [field]: '' }));
        }
    };

    const validateForm = () => {
        const newErrors: { [key: string]: string } = {};

        if (!formData.first_name.trim()) newErrors.first_name = 'First name is required';
        if (!formData.last_name.trim()) newErrors.last_name = 'Last name is required';
        if (!formData.email.trim()) {
            newErrors.email = 'Email is required';
        } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(formData.email)) {
            newErrors.email = 'Valid email is required';
        }
        if (!formData.tenant_id) newErrors.tenant_id = 'Tenant selection is required';
        if (!formData.role_id) newErrors.role_id = 'Role selection is required';

        if (!formData.generatePassword && (!formData.customPassword || formData.customPassword.length < 8)) {
            newErrors.customPassword = 'Password must be at least 8 characters';
        }

        setErrors(newErrors);
        return Object.keys(newErrors).length === 0;
    };

    const handleCreate = () => {
        if (validateForm()) {
            const payload: UserCreateCrossTenant = {
                first_name: formData.first_name,
                last_name: formData.last_name,
                email: formData.email,
                tenant_id: formData.tenant_id,
                role_id: formData.role_id,
                password: formData.generatePassword ? undefined : formData.customPassword,
                is_active: true
            };
            onSave(payload);
        }
    };

    const handleCancel = () => {
        setFormData({
            first_name: '',
            last_name: '',
            email: '',
            tenant_id: '',
            role_id: '',
            generatePassword: true,
            customPassword: ''
        });
        setErrors({});
        onCancel();
    };

    return (
        <Transition.Root show={isOpen} as={Fragment}>
            <Dialog as="div" className="relative z-50" initialFocus={cancelButtonRef} onClose={handleCancel}>
                <Transition.Child
                    as={Fragment}
                    enter="ease-out duration-300"
                    enterFrom="opacity-0"
                    enterTo="opacity-100"
                    leave="ease-in duration-200"
                    leaveFrom="opacity-100"
                    leaveTo="opacity-0"
                >
                    <div className="fixed inset-0 bg-gray-500 bg-opacity-75 transition-opacity" />
                </Transition.Child>

                <div className="fixed inset-0 z-10 overflow-y-auto">
                    <div className="flex min-h-full items-end justify-center p-4 text-center sm:items-center sm:p-0">
                        <Transition.Child
                            as={Fragment}
                            enter="ease-out duration-300"
                            enterFrom="opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95"
                            enterTo="opacity-100 translate-y-0 sm:scale-100"
                            leave="ease-in duration-200"
                            leaveFrom="opacity-100 translate-y-0 sm:scale-100"
                            leaveTo="opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95"
                        >
                            <Dialog.Panel className="relative transform overflow-hidden rounded-xl bg-white text-left shadow-2xl transition-all sm:my-8 sm:w-full sm:max-w-lg">
                                <div className="bg-white px-4 pb-4 pt-5 sm:p-6 sm:pb-4">
                                    <div className="sm:flex sm:items-start">
                                        <div className="mx-auto flex h-12 w-12 flex-shrink-0 items-center justify-center rounded-full bg-blue-100 sm:mx-0 sm:h-10 sm:w-10">
                                            <UserPlus className="h-6 w-6 text-blue-600" aria-hidden="true" />
                                        </div>
                                        <div className="mt-3 text-center sm:ml-4 sm:mt-0 sm:text-left w-full">
                                            <Dialog.Title as="h3" className="text-xl font-bold leading-6 text-gray-900 mb-4">
                                                Create New User
                                            </Dialog.Title>

                                            <div className="space-y-4">
                                                <div className="grid grid-cols-2 gap-4">
                                                    <div>
                                                        <label className="block text-sm font-medium text-gray-700 mb-1">First Name</label>
                                                        <Input
                                                            value={formData.first_name}
                                                            onChange={(e) => handleInputChange('first_name', e.target.value)}
                                                            placeholder="John"
                                                            className={errors.first_name ? 'border-red-500' : ''}
                                                        />
                                                        {errors.first_name && <p className="text-xs text-red-500 mt-1">{errors.first_name}</p>}
                                                    </div>
                                                    <div>
                                                        <label className="block text-sm font-medium text-gray-700 mb-1">Last Name</label>
                                                        <Input
                                                            value={formData.last_name}
                                                            onChange={(e) => handleInputChange('last_name', e.target.value)}
                                                            placeholder="Doe"
                                                            className={errors.last_name ? 'border-red-500' : ''}
                                                        />
                                                        {errors.last_name && <p className="text-xs text-red-500 mt-1">{errors.last_name}</p>}
                                                    </div>
                                                </div>

                                                <div>
                                                    <label className="block text-sm font-medium text-gray-700 mb-1">Email Address</label>
                                                    <Input
                                                        type="email"
                                                        value={formData.email}
                                                        onChange={(e) => handleInputChange('email', e.target.value)}
                                                        placeholder="john.doe@example.com"
                                                        className={errors.email ? 'border-red-500' : ''}
                                                    />
                                                    {errors.email && <p className="text-xs text-red-500 mt-1">{errors.email}</p>}
                                                </div>

                                                <div className="grid grid-cols-2 gap-4">
                                                    <div>
                                                        <label className="block text-sm font-medium text-gray-700 mb-1 flex items-center gap-1">
                                                            <Globe className="h-3 w-3" /> Tenant
                                                        </label>
                                                        <Select
                                                            value={formData.tenant_id}
                                                            onValueChange={(val) => handleInputChange('tenant_id', val)}
                                                            disabled={fetchingData}
                                                        >
                                                            <SelectTrigger className={errors.tenant_id ? 'border-red-500' : ''}>
                                                                <SelectValue placeholder={fetchingData ? "Loading..." : "Select Tenant"} />
                                                            </SelectTrigger>
                                                            <SelectContent>
                                                                {tenants.map(t => (
                                                                    <SelectItem key={t.id} value={t.id}>{t.name}</SelectItem>
                                                                ))}
                                                            </SelectContent>
                                                        </Select>
                                                        {errors.tenant_id && <p className="text-xs text-red-500 mt-1">{errors.tenant_id}</p>}
                                                    </div>
                                                    <div>
                                                        <label className="block text-sm font-medium text-gray-700 mb-1 flex items-center gap-1">
                                                            <Shield className="h-3 w-3" /> Role
                                                        </label>
                                                        <Select
                                                            value={formData.role_id}
                                                            onValueChange={(val) => handleInputChange('role_id', val)}
                                                            disabled={fetchingData}
                                                        >
                                                            <SelectTrigger className={errors.role_id ? 'border-red-500' : ''}>
                                                                <SelectValue placeholder={fetchingData ? "Loading..." : "Select Role"} />
                                                            </SelectTrigger>
                                                            <SelectContent>
                                                                {roles.map(r => (
                                                                    <SelectItem key={r.id} value={r.id}>{r.displayName || r.name}</SelectItem>
                                                                ))}
                                                            </SelectContent>
                                                        </Select>
                                                        {errors.role_id && <p className="text-xs text-red-500 mt-1">{errors.role_id}</p>}
                                                    </div>
                                                </div>

                                                <div className="pt-4 border-t border-gray-100">
                                                    <p className="text-sm font-semibold text-gray-900 mb-2 flex items-center gap-1">
                                                        <Lock className="h-4 w-4" /> Password Strategy
                                                    </p>
                                                    <div className="space-y-3">
                                                        <label className="flex items-start gap-2 cursor-pointer p-2 rounded hover:bg-gray-50">
                                                            <input
                                                                type="radio"
                                                                className="mt-1"
                                                                checked={formData.generatePassword}
                                                                onChange={() => handleInputChange('generatePassword', true)}
                                                            />
                                                            <div>
                                                                <p className="text-sm font-medium">Auto-generate secure password</p>
                                                                <p className="text-xs text-gray-500">System will create a random password and send it via email if configured.</p>
                                                            </div>
                                                        </label>
                                                        <label className="flex items-start gap-2 cursor-pointer p-2 rounded hover:bg-gray-50">
                                                            <input
                                                                type="radio"
                                                                className="mt-1"
                                                                checked={!formData.generatePassword}
                                                                onChange={() => handleInputChange('generatePassword', false)}
                                                            />
                                                            <div className="flex-1">
                                                                <p className="text-sm font-medium">Set custom password</p>
                                                                {!formData.generatePassword && (
                                                                    <div className="mt-2">
                                                                        <Input
                                                                            type="password"
                                                                            value={formData.customPassword}
                                                                            onChange={(e) => handleInputChange('customPassword', e.target.value)}
                                                                            placeholder="Minimum 8 characters"
                                                                            className={errors.customPassword ? 'border-red-500' : ''}
                                                                        />
                                                                        {errors.customPassword && <p className="text-xs text-red-500 mt-1">{errors.customPassword}</p>}
                                                                    </div>
                                                                )}
                                                            </div>
                                                        </label>
                                                    </div>
                                                </div>
                                            </div>
                                        </div>
                                    </div>
                                </div>
                                <div className="bg-gray-50 px-4 py-3 sm:flex sm:flex-row-reverse sm:px-6 gap-2">
                                    <Button
                                        onClick={handleCreate}
                                        disabled={isLoading || fetchingData}
                                        className="w-full sm:w-auto bg-blue-600 hover:bg-blue-700"
                                    >
                                        {isLoading ? 'Creating...' : 'Create User'}
                                    </Button>
                                    <Button
                                        variant="outline"
                                        onClick={handleCancel}
                                        ref={cancelButtonRef}
                                        disabled={isLoading}
                                        className="w-full sm:w-auto mt-3 sm:mt-0"
                                    >
                                        Cancel
                                    </Button>
                                </div>
                            </Dialog.Panel>
                        </Transition.Child>
                    </div>
                </div>
            </Dialog>
        </Transition.Root>
    );
}
